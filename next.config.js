/** @type {import('next').NextConfig} */

// NOTA: la Content-Security-Policy se define en middleware.ts (CSP basada en
// nonce por request), no aquí. Mantener una CSP estática en este archivo
// duplicaría el header y reintroduciría 'unsafe-inline'. Aquí quedan solo los
// headers de seguridad que no dependen de un nonce por request.
const securityHeaders = [
  { key: 'X-Frame-Options', value: 'DENY' },
  { key: 'X-Content-Type-Options', value: 'nosniff' },
  { key: 'Referrer-Policy', value: 'strict-origin-when-cross-origin' },
  { key: 'Permissions-Policy', value: 'camera=(), microphone=(), geolocation=()' },
  { key: 'X-DNS-Prefetch-Control', value: 'on' },
  {
    key: 'Strict-Transport-Security',
    value: 'max-age=63072000; includeSubDomains; preload',
  },
];

const nextConfig = {
  reactStrictMode: true,
  async headers() {
    return [
      {
        source: '/(.*)',
        headers: securityHeaders,
      },
    ];
  },
};

module.exports = nextConfig;
